fn main() {
    fizzbuzz(5, 10);
}

fn fizzbuzz(start: i32, end: i32) {
    for i in start..=end {
        if i % 3 == 0 && i % 5 == 0 {
            println!("FizzBuzz");
        } else if i % 3 == 0 {
            println!("Fizz");
        } else if i % 5 == 0 {
            println!("Buzz");
        } else {
            println!("{i}");
        }
    }
}
